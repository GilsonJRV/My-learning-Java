public class ConcatenarporPartes {
    public static void main(String[] args) {
        // Declaração e inicialização de variáveis do tipo String
        String part1 = "I";
        String part2 = "n";
        String part3 = "t";
        String part4 = "er";
        String part5 = "n";
        String part6 = "et";
        
        // Concatenação das variáveis e impressão do resultado
        System.out.println(part1+part2+part3+part4+part5+part6);

        // Declaração e inicialização de variáveis do tipo String para formar um nome
        String a = "G";
        String b = "I";
        String c = "L";
        String d = "S";
        String e = "O";
        String f = "N";

        String g = "J";
        String h = "U";
        String i = "N";
        String j = "I";
        String k = "O";
        String l = "R";
        
        String m = "V";
        String n = "A";
        String o = "L";
        String p = "A";
        String q = "D";
        String r = "A";
        String s = "R";
        String t = "E";
        String u = "S";       
       
        // Concatenação das variáveis para formar o nome completo e impressão do resultado
        System.out.println(a+b+c+d+e+f+" "+g+h+i+j+k+l+" "+m+n+o+p+q+r+s+t+u);
                
    }    
}